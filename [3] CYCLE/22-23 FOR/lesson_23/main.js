for (let i = 0; i < 10; i++) {
    if (i === 5) {
        break; // Прерывание цикла, когда i равно 5
    }
    console.log(i); // Вывод значений переменной i до прерывания цикла
}

// Вывод:
// 0
// 1
// 2
// 3
// 4

for (let i = 0; i < 10; i++) {
    if (i % 2 === 0) {
        continue; // Пропуск текущей итерации, если i четное
    }
    console.log(i); // Вывод значений переменной i, которые нечетные
}

// Вывод:
// 1
// 3
// 5
// 7
// 9

// Объяснение и архитектура:
// 1. Цикл for состоит из трех частей: инициализация (let i = 0), условие продолжения (i < 10) и выражение после каждой итерации (i++).
// 2. Оператор break:
//    - Используется для прерывания выполнения цикла.
//    - Когда условие if (i === 5) истинно, цикл прерывается, и выполнение продолжается после цикла.
// 3. Оператор continue:
//    - Используется для пропуска текущей итерации цикла и перехода к следующей.
//    - Когда условие if (i % 2 === 0) истинно, текущая итерация пропускается, и выполнение продолжается с следующей итерации.

// Дополнительно:
// 1. Оператор break можно использовать в любом цикле (for, while, do...while) и в switch.
// 2. Оператор continue также можно использовать в любом цикле (for, while, do...while).
// 3. Использование break и continue помогает улучшить читаемость и контроль потока выполнения кода.
