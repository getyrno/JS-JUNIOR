console.log( 2 > 1 );

// console.log: Выводит сообщение в консоль браузера.
// 2 > 1: Оператор сравнения, проверяющий, больше ли 2, чем 1.
// Результат: true
// Тип данных: Boolean
// Особенности:
//     Оператор > возвращает true, если левый операнд больше правого.

console.log( 10 < 5 );

// console.log: Выводит сообщение в консоль браузера.
// 10 < 5: Оператор сравнения, проверяющий, меньше ли 10, чем 5.
// Результат: false
// Тип данных: Boolean
// Особенности:
//     Оператор < возвращает true, если левый операнд меньше правого.

let num1 = 5;

// let: Объявляет переменную с областью видимости в блоке кода.
// num1: Имя переменной, которая содержит значение 5.
// 5: Присвоенное значение типа Number.
// Тип данных: Number
// Особенности:
//     Переменной num1 присваивается числовое значение 5.

let num2 = '5';

// let: Объявляет переменную с областью видимости в блоке кода.
// num2: Имя переменной, которая содержит строку '5'.
// '5': Присвоенное значение типа String.
// Тип данных: String
// Особенности:
//     Переменной num2 присваивается строка '5'.

console.log(typeof(num1));

// console.log: Выводит сообщение в консоль браузера.
// typeof(num1): Оператор typeof возвращает строку, указывающую тип операнда num1.
// Результат: 'number'
// Тип данных: String
// Особенности:
//     Оператор typeof возвращает строку 'number' для переменной num1, поскольку она содержит числовое значение.

console.log(typeof(num2));

// console.log: Выводит сообщение в консоль браузера.
// typeof(num2): Оператор typeof возвращает строку, указывающую тип операнда num2.
// Результат: 'string'
// Тип данных: String
// Особенности:
//     Оператор typeof возвращает строку 'string' для переменной num2, поскольку она содержит строковое значение.

console.log(num1 == num2);

// console.log: Выводит сообщение в консоль браузера.
// num1 == num2: Оператор нестрогого равенства, проверяющий, равны ли значения num1 и num2 после приведения типов.
// Результат: true
// Тип данных: Boolean
// Особенности:
//     Оператор == приводит оба операнда к одному типу перед сравнением, поэтому 5 (Number) и '5' (String) считаются равными.

console.log(num1 === num2);

// console.log: Выводит сообщение в консоль браузера.
// num1 === num2: Оператор строгого равенства, проверяющий, равны ли значения num1 и num2 без приведения типов.
// Результат: false
// Тип данных: Boolean
// Особенности:
//     Оператор === не приводит операнды к одному типу перед сравнением, поэтому 5 (Number) и '5' (String) считаются неравными.

console.log(num1 != num2);

// console.log: Выводит сообщение в консоль браузера.
// num1 != num2: Оператор нестрогого неравенства, проверяющий, не равны ли значения num1 и num2 после приведения типов.
// Результат: false
// Тип данных: Boolean
// Особенности:
//     Оператор != приводит оба операнда к одному типу перед сравнением, поэтому 5 (Number) и '5' (String) считаются равными, и результат будет false.

console.log(num1 !== num2);

// console.log: Выводит сообщение в консоль браузера.
// num1 !== num2: Оператор строгого неравенства, проверяющий, не равны ли значения num1 и num2 без приведения типов.
// Результат: true
// Тип данных: Boolean
// Особенности:
//     Оператор !== не приводит операнды к одному типу перед сравнением, поэтому 5 (Number) и '5' (String) считаются неравными, и результат будет true.

// Архитектура и работа:
//     Операторы > и < используются для сравнения чисел и возвращают true или false в зависимости от результата сравнения.
//     Оператор typeof возвращает строку, указывающую тип операнда.
//     Операторы == и != выполняют нестрогое сравнение, приводя операнды к одному типу перед сравнением.
//     Операторы === и !== выполняют строгое сравнение, не приводя операнды к одному типу перед сравнением.
