document.querySelector('.btn').addEventListener('click', (e) => {
    num--;
    if (num === 9 && num % 2 === 0 || num % 3 === 0) {
        console.log(true);
    } else {
        console.log(false);
    }
    document.querySelector('span').textContent = num;
    if (document.querySelector('span').textContent === '5') {
        alert(num);
    }
});

// document.querySelector('.btn').addEventListener('click', (e) => {...}):
//     document.querySelector('.btn'): Выбирает элемент с классом 'btn' в документе.
//     addEventListener('click', (e) => {...}): Добавляет обработчик события 'click' к выбранному элементу.
//     (e) => {...}: Функция-стрелка, которая будет выполнена при каждом клике на элемент с классом 'btn'.

