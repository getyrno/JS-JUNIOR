let score = 0;

// let: Объявляет переменную с областью видимости в блоке кода.
// score: Имя переменной, которая содержит значение 0.
// 0: Присвоенное значение типа Number.
// Тип данных: Number
// Особенности:
//     Переменной score присваивается числовое значение 0.
//     Переменная score будет использоваться для хранения текущего счета.

document.querySelector("body").addEventListener("click", () => {
    score++;
    document.querySelector("h1").textContent = score;
});

// document.querySelector("body"): Выбирает элемент <body> в документе.
// addEventListener("click", ...): Добавляет обработчик события "click" к элементу <body>.
// () => { ... }: Функция-стрелка, которая будет вызвана при каждом клике по элементу <body>.
//     score++: Увеличивает значение переменной score на 1 при каждом клике.
//     document.querySelector("h1").textContent = score: Находит элемент <h1> и обновляет его текстовое содержимое текущим значением переменной score.
// Тип данных:
//     document.querySelector: Возвращает элемент DOM.
//     addEventListener: Метод объекта Element, добавляющий обработчик события.
//     () => {}: Функция-стрелка, используемая как обработчик события.

// Особенности:
//     document.querySelector используется для поиска первого элемента, соответствующего указанному селектору CSS (в данном случае "body" и "h1").
//     addEventListener добавляет обработчик события "click", который будет вызываться при каждом клике по элементу <body>.
//     Функция-стрелка является сокращенной записью функции и наследует значение this из окружающего контекста.
//     score++ увеличивает значение переменной score на 1 при каждом клике.
//     document.querySelector("h1").textContent = score обновляет текстовое содержимое первого элемента <h1> текущим значением переменной score.

// Архитектура и работа:
//     Переменная score инициализируется значением 0 и используется для отслеживания количества кликов.
//     Метод document.querySelector используется для выбора элементов <body> и <h1>.
//     addEventListener добавляет обработчик события "click" к элементу <body>.
//     Обработчик события (функция-стрелка) увеличивает значение переменной score на 1 при каждом клике и обновляет текстовое содержимое элемента <h1> текущим значением переменной score.
